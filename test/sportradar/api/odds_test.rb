require 'test_helper'

class Sportradar::Api::OddsTest < Minitest::Test

  def test_it_accepts_a_valid_access_level
    good = Sportradar::Api::Odds.new('p')
    assert_kind_of Sportradar::Api::Odds, good
    assert_raises Sportradar::Api::Error::InvalidAccessLevel do
      Sportradar::Api::Odds.new('dumb')
    end
  end

  def test_it_makes_a_good_odds_request
    VCR.use_cassette("tests odds request") do
      request = Sportradar::Api::Odds.new( 't').odds
      assert_kind_of Sportradar::Api::Error, request # This will fail until I get an active odds api key
    end
  end

end
